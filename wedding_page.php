standing to her right. At the altar, the priest awaits who then congratulates them and reads verses from
the Holy Bible, which is followed by sermon called Homily. Then the priest asks the consent of both, bride
and groom for the marriage. The couples exchange rings and seek the blessings of the priest and family. 
